# ================================================
# MAIN
# ================================================

# Mode CLI: argument langsung
if [ $# -gt 0 ]; then
    case "$1" in
        admin)   collect_admin ;;
        user)    collect_user ;;
        rapor)   collect_rapor ;;
        auth)    collect_auth ;;
        shared)  collect_shared ;;
        all)     collect_all ;;
        *)
            echo -e "${RED}❌ Unknown feature: $1${NC}"
            echo ""
            echo -e "Usage: ${BOLD}$0${NC} [admin|user|rapor|auth|shared|all]"
            echo -e "       (no argument = interactive menu)"
            exit 1
            ;;
    esac
    exit 0
fi

# Mode interactive
