#!/bin/bash

# ================================================
# E-RAPORT PER-FEATURE COLLECTOR v1
# ================================================
# Interactive picker — pilih feature mana yang mau di-collect.
#
# Mode tersedia:
#   1. ADMIN          → Master Data, Akademik, Sistem
#   2. USER           → Penilaian / Input Nilai harian
#   3. RAPOR          → Cetak / Preview / ZIP archive
#   4. AUTH           → Login flow + auth callback
#   5. SHARED CORE    → Layout, providers, hooks dasar, types
#   6. ALL FEATURES   → Gabungan semua (1 file per feature)
#
# Usage:
#   ./collect-per-feat.sh              # interactive menu
#   ./collect-per-feat.sh admin        # langsung admin
#   ./collect-per-feat.sh user         # langsung user
#   ./collect-per-feat.sh rapor        # langsung rapor
#   ./collect-per-feat.sh auth         # langsung auth
#   ./collect-per-feat.sh shared       # langsung shared core
#   ./collect-per-feat.sh all          # collect semua feature
#
# OUTPUT: collection/collected-<feature>.txt
# ================================================

# ── Colors ──────────────────────────────────────
GREEN='\033[0;32m'
BLUE='\033[0;34m'
YELLOW='\033[1;33m'
CYAN='\033[0;36m'
RED='\033[0;31m'
MAGENTA='\033[0;35m'
BOLD='\033[1m'
NC='\033[0m'

# ── Paths ────────────────────────────────────────
WORKSPACE_ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
SRC="$WORKSPACE_ROOT/src"
COLLECTION_DIR="$WORKSPACE_ROOT/collection"

# ── State (per run) ──────────────────────────────
SKIPPED=0
COLLECTED=0
CURRENT_OUT=""

# ── Init ─────────────────────────────────────────
mkdir -p "$COLLECTION_DIR"

# ── Helpers ──────────────────────────────────────

init_output() {
    local feature="$1"
    local description="$2"
    CURRENT_OUT="$COLLECTION_DIR/collected-${feature}.txt"
    SKIPPED=0
    COLLECTED=0

    {
        echo "# E-RAPORT - ${description}"
        echo "# Generated by collect-per-feat.sh"
        echo ""
        echo "---"
        echo ""
    } > "$CURRENT_OUT"
}

section() {
    local icon="$1"
    local label="$2"
    echo ""
    echo -e "${BOLD}${BLUE}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${NC}"
    echo -e "${BOLD}${BLUE}${icon} ${label}${NC}"
    echo -e "${BOLD}${BLUE}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${NC}"
    echo "" >> "$CURRENT_OUT"
    echo "# ${icon} ${label}" >> "$CURRENT_OUT"
    echo "" >> "$CURRENT_OUT"
}

collect_file() {
    local file="$1"
    local rel="${file#$WORKSPACE_ROOT/}"
    local ext="${file##*.}"

    if [ ! -f "$file" ]; then
        echo -e "  ${RED}✗ SKIP${NC} ${YELLOW}(not found)${NC}: $rel"
        SKIPPED=$((SKIPPED + 1))
        return
    fi

    local lines
    lines=$(wc -l < "$file" 2>/dev/null)
    lines="${lines// /}"

    echo -e "  ${GREEN}✓${NC} $rel ${CYAN}(${lines} lines)${NC}"
    COLLECTED=$((COLLECTED + 1))

    echo "" >> "$CURRENT_OUT"
    echo "## \`${rel}\`" >> "$CURRENT_OUT"
    echo "" >> "$CURRENT_OUT"
    echo "**Lines:** ${lines}" >> "$CURRENT_OUT"
    echo "" >> "$CURRENT_OUT"
    echo '```'"${ext}" >> "$CURRENT_OUT"
    cat "$file" >> "$CURRENT_OUT"
    echo "" >> "$CURRENT_OUT"
    echo '```' >> "$CURRENT_OUT"
    echo "" >> "$CURRENT_OUT"
    echo "---" >> "$CURRENT_OUT"
    echo "" >> "$CURRENT_OUT"
}

print_summary() {
    local feature="$1"
    echo ""
    echo -e "${BOLD}${BLUE}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${NC}"
    echo -e "${BOLD}${GREEN}✅ SELESAI - ${feature^^}${NC}"
    echo -e "${BOLD}${BLUE}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${NC}"
    echo ""
    echo -e "${GREEN}📄 Output    :${NC} $CURRENT_OUT"
    echo -e "${CYAN}📝 Lines     :${NC} $(wc -l < "$CURRENT_OUT")"
    echo -e "${CYAN}📦 Size      :${NC} $(du -h "$CURRENT_OUT" | cut -f1)"
    echo -e "${GREEN}✓  Collected :${NC} $COLLECTED files"
    echo -e "${YELLOW}✗  Skipped   :${NC} $SKIPPED files (not found)"
    echo ""
}

