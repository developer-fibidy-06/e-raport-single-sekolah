    echo -e "${BOLD}${MAGENTA}╔═══════════════════════════════════════════════════╗${NC}"
    echo -e "${BOLD}${MAGENTA}║   E-RAPORT PER-FEATURE COLLECTOR                  ║${NC}"
    echo -e "${BOLD}${MAGENTA}╚═══════════════════════════════════════════════════╝${NC}"
    echo ""
    echo -e "${CYAN}Pilih feature yang mau di-collect:${NC}"
    echo ""
    echo -e "  ${BOLD}1)${NC} ${YELLOW}🏫 ADMIN${NC}        — Master Data, Akademik, Sistem"
    echo -e "  ${BOLD}2)${NC} ${YELLOW}📝 USER${NC}         — Penilaian / Input Nilai harian"
    echo -e "  ${BOLD}3)${NC} ${YELLOW}📊 RAPOR${NC}        — Cetak / Preview / ZIP archive"
    echo -e "  ${BOLD}4)${NC} ${YELLOW}🔐 AUTH${NC}         — Login flow + session"
    echo -e "  ${BOLD}5)${NC} ${YELLOW}🧩 SHARED CORE${NC}  — Layout, providers, hooks dasar, types"
    echo -e "  ${BOLD}6)${NC} ${GREEN}🎯 ALL${NC}          — Generate semua (5 file terpisah)"
